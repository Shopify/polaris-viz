export function formatHourlyLabel(value: string) {
  const formatter = new Intl.DateTimeFormat('en', {
    timeStyle: 'short',
  });

  return formatter
    .format(new Date(value))
    .toLocaleLowerCase()
    .replace('am', 'a.m.')
    .replace('pm', 'p.m.');
}
